
def area(a):
    '''
    Возвращает площадь квадрата.

        Параметры:
            a (float) : сторона квадрата

        Возвращаемое значение:
            S (float) : площадь квадрата
    '''
    S = a * a
    return S


def perimeter(a):
    '''
    Возвращает периметр квадрата.

        Параметры:
            a (float) : сторона квадрата

        Возвращаемое значение:
            P (float) : периметр квадрата
    '''
    P = 4 * a
    return P
